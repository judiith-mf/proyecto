    private void configTabla(){ 
    
        String col[]={"ID","DNI","NOMBRE","APELLIDO1","APELLIDO2","PARENTESCO"};
        
        DefaultTableModel modelo=new DefaultTableModel(col,0){
        
              @Override
              public boolean isCellEditable(int row, int column){
                  return false;
              }
        
        };
        
        jtAutorizados.setModel(modelo);
        jtAutorizados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                
    }
    
    public void cargaTabla(){
        DefaultTableModel modelo=(DefaultTableModel)jtAutorizados.getModel();
        
        AutorizadoDaoImp autControler=AutorizadoDaoImp.getInstance();
        String[] fila=new String[6];
        
        modelo.setNumRows(0);
        try{
            List<Autorizado> lst=autControler.getAll();
            
            for( Autorizado aut :lst){
                fila[0]=""+aut.getId();
                fila[1]=""+aut.getDni();
                fila[2]=""+aut.getNombre();
                fila[3]=""+aut.getApellido1();
                fila[4]="" + aut.getApellido2();
                
                Parentesco parent = aut.getParentesco();
                        
                 
                fila[5]="" + parent.toString();
                modelo.addRow(fila);
            }
            //selecciono la primera fila
           jtAutorizados.setRowSelectionInterval(0,0); 
           
        }catch(Exception e){
            System.out.println("Errorr:"+e.getMessage());
        }
    }
    
