    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){
            DefaultTableModel modelo=(DefaultTableModel) jtAutorizados.getModel();
            TableRowSorter<TableModel> trSorter = new TableRowSorter<TableModel>(modelo);

            jtAutorizados.setRowSorter(trSorter);

            if (txtBuscar.getText().length()==0) {
                trSorter.setRowFilter(null);
            }else{
                trSorter.setRowFilter(RowFilter.regexFilter(txtBuscar.getText().trim()));
            }

        }
    }//GEN-LAST:event_txtBuscarKeyPressed

    private void jtAutorizadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAutorizadosMouseClicked
        if (evt.getClickCount()==2){
            try {
                // System.out.println("Doble click.....");
                //Cargar el detalle de un autorizado cuadno se le da doble click
                JDialog frame=new JDialog();
                frame.setTitle("Detalle Autorizado");
                frame.setModal(true);

                jpAutorizadoDetalle panel=new jpAutorizadoDetalle();

                int idseleccion=Integer.parseInt(jtAutorizados.getValueAt(jtAutorizados.getSelectedRow(), 0).toString());

                panel.CargaDetalle(idseleccion);
                panel.frmAut=this;

                frame.getContentPane().add(panel);
                frame.pack();
                frame.setVisible(true);
                
            } catch (Exception ex) {
                Logger.getLogger(jpAlumnoDetalle.class.getName()).log(Level.SEVERE, null, ex);
            }

        }        // TODO add your handling code here:
    }//GEN-LAST:event_jtAutorizadosMouseClicked

